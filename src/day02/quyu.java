package day02;

public class quyu {
    public static void main(String[] args) {
        System.out.println(15%4);//3
        //15-(4*3)=3
        System.out.println(-15%4);//-3
        //-15-(4*-3)=-3
        System.out.println(15%-4);//3
        //15-(-4*-3)=3
        System.out.println(-15%-4);//-3
        //-15-(-4*3)=-3
        System.out.println("1+1="+1+1);//1+1=11
    }
}
