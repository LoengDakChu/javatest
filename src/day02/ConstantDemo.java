package day02;

public class ConstantDemo {
    public static void main(String[] args) {
        //输出整数常量
        System.out.println(111);
        //输出小数常量
        System.out.println(0.123);
        //输出负数常量
        System.out.println(-5);
        //输出字符常量
        System.out.println('a');
        //输出布尔常量
        System.out.println(true);
        //输出字符串常量
        System.out.println("你好java");
    }
}
